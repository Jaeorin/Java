package study;

public class Var1_181218 {
	public static void main(String args[]) {
		//타입
		System.out.println("안녕 자바"); //문자열 String
		System.out.println(7); //정수 int(integer)
		System.out.println(9.8); //실수 float, double
		System.out.println('가'); //문자 char(character)
		System.out.println(true); //boolean
		System.out.println(false); //참, 거짓

		//타입지정 + 변수 = 값
		String name="니코";
		System.out.println(name);
		
		int num=80;
		System.out.println(num);
		
		char ch='a';
		System.out.println(ch);
		
		boolean check=true; //경우의 수가 2가지(true, false)
		System.out.println(check);
		
		double number=10.8;
		System.out.println(number);
		
	}
}
